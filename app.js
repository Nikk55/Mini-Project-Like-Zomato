  // Check Restaurant When Open at Current Time
  document.getElementById("Open").addEventListener("click",()=>{
    const now=new Date();
    const currentTime=now.getHours();
    const result = restaurants.filter((obj)=>{
      const {Restaurant_open_time,Restaurant_close_time}=obj;

      // If the closing time is after midnight, adjust comparison
      if(Restaurant_open_time>Restaurant_close_time) {
        return currentTime>=Restaurant_open_time || currentTime<Restaurant_close_time;
      }
      return currentTime>=Restaurant_open_time && currentTime<Restaurant_close_time;

    });
    document.getElementById("root").replaceChildren();
    getrestaurant(result);
  })

